package com.github.aqiu202.entity;

/**
 * 工作中心
 */
public class BaseWorkCenter {

    /**
     * 工作中心编号
     */
    private String code;
    /**
     * 工作中心名称
     */
    private String name;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
